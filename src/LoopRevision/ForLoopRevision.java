package LoopRevision;

public class ForLoopRevision {

	public static void main(String[] args) {
		// PRINT 2 TABLE 
		
		for(int a=2; a<=20; a=a+2) 
		{
			System.out.println(a);
		}
		
		// print hello 5 times
		
		String msg= "Hello";
		for(int i=1; i<=5; i++) 
		{
			System.out.println(msg);
		}

	}

}
